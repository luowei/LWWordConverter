    private static var decryptDictionary: [String: String] = {
        var dict: [String: String] = [:]
        for (key, value) in encryptDictionary {
            dict[value] = key
        }
        return dict
    }()

    // MARK: - Public Methods

    /// 判断某段文本是否包含加密字符
    public static func isEncryptString(_ str: String?) -> Bool {
        guard let str = str else {
            return false
        }
        return str.contains(divStr)
    }

    /// 把原始文本插入转译到新的文本 "你好"->"（你好）再见" 括号内文本不可见
    public static func encryptText(_ primevalText: String?, displayText: String?) -> String? {
        guard let primevalText = primevalText else {
            return nil
        }

        if primevalText.isEmpty {
            return ""
        }

        guard let data = primevalText.data(using: .utf8) else {
            return nil
        }

        let base64 = data.base64EncodedString()
        var retStr = ""

        for char in base64 {
            let charStr = String(char)
            if let encoded = encryptDictionary[charStr] {
                retStr += encoded
            }
        }

        retStr += divStr
        if let displayText = displayText {
            retStr += displayText
        }

        return retStr
    }

    /// 把加密文本过滤提取，并转译到原始文本 "（你好）再见"->"你好"
    public static func decryptText(_ imitativeText: String?) -> String? {
        guard let imitativeText = imitativeText else {
            return nil
        }

        var input = imitativeText

        // Find divStr from the end
        guard let range = input.range(of: divStr, options: .backwards) else {
            return imitativeText
        }

        // Remove everything after divStr
        input = String(input[..<range.lowerBound])

        if input.isEmpty {
            return ""
        }

        var base64 = ""
        var idx = input.startIndex

        while idx < input.endIndex {
            let endIdx = input.index(idx, offsetBy: 3, limitedBy: input.endIndex) ?? input.endIndex
            let substring = String(input[idx..<endIdx])

            if let refStr = decryptDictionary[substring] {
                base64 += refStr
            }

            if endIdx == input.endIndex {
                break
            }
            idx = endIdx
        }

        guard let data = Data(base64Encoded: base64, options: .ignoreUnknownCharacters),
              let text = String(data: data, encoding: .utf8) else {
            return nil
        }

        return text
    }

    /// 选择一种加密的套路
    public static func saveEncryptMode(_ encryptMode: EncryptMode) {
        UserDefaults.standard.set(encryptMode.rawValue, forKey: "EncryptMode")
        UserDefaults.standard.synchronize()
    }

    /// 获取当前选择的加密套路
    public static func getEncryptMode() -> EncryptMode {
        let rawValue = UserDefaults.standard.integer(forKey: "EncryptMode")
        return EncryptMode(rawValue: rawValue) ?? .emoji
    }

